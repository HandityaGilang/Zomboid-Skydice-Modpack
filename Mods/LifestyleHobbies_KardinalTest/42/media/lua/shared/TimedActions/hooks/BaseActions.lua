local ogAdjustTime = ISBaseTimedAction.adjustMaxTime
function ISBaseTimedAction:adjustMaxTime(maxTime)
    if self.ignoreDynamicTime then return maxTime; end
    return ogAdjustTime(self, maxTime)
end
