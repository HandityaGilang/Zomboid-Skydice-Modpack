function getExploredPOI(x, y, list)
    local poi
    for k, v in pairs(poiList) do
        local poiString = v.name.."- x:"..tostring(v.x).."- y:"..tostring(v.y)
        if not list[poiString] and x >= v.x-v.rangeX and x <= v.x+v.rangeX and y >= v.y-v.rangeY and y<=v.y+v.rangeY then poi = poiString; break; end
    end
    return poi
end
